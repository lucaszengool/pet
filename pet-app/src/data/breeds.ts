export function groupBreedsByLetter(breeds: BreedItem[]): BreedGroup[] {
  const grouped: Record<string, BreedItem[]> = {}
  breeds.forEach(b => {
    const letter = b.pinyin.toUpperCase()
    if (!grouped[letter]) grouped[letter] = []
    grouped[letter].push(b)
  })
  return Object.keys(grouped)
    .sort()
    .map(letter => ({ letter, breeds: grouped[letter] }))
}

export function getHotBreeds(type: 'cat' | 'dog' | 'other'): BreedItem[] {
  const map = { cat: catBreeds, dog: dogBreeds, other: otherBreeds }
  return map[type].filter(b => b.hot)
}

export function getBreedsByType(type: 'cat' | 'dog' | 'other'): BreedItem[] {
  const map = { cat: catBreeds, dog: dogBreeds, other: otherBreeds }
  return map[type]
}

export function searchBreeds(type: 'cat' | 'dog' | 'other', query: string): BreedItem[] {
  const breeds = getBreedsByType(type)
  if (!query) return breeds
  return breeds.filter(b => b.name.includes(query))
}
